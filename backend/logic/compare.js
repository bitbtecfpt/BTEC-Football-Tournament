function matchResult(aScore, bScore) {
    if (aScore > bScore) {
        return 1;
    } else if (aScore < bScore) {
        return 2;
    } else {
        return 3;
    }
}

function TotalScore(aScore, bScore) {
    if (aScore > bScore) {
        return 1;
    } else if (aScore < bScore) {
        return 2;
    } else {
        return 3;
    }
}